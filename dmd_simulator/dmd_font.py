    def putletter(self,image: Image,letter,rotation: int,x: int,y: int,color):
        """
        Parameters
        ----------
        image : Image
            The image where to write the letter
        letter : str
            The letter to write
        rotation : int
            The rotation (clockwize)
            - 0: no rotation
            - 1: 90°
            - 2: 180° rotation
            - 3: 270°
        x : int
            The x coordonate
        y : int
            The y coordonate
        color: ((red:int, blue:int, green: int))
            The letter color
        """        
        height=self.fonts[self.font_name]["height"]
        width=self.fonts[self.font_name]["width"]

        for y1 in range(height):        
            for x1 in range(width):
                if (self.fonts[self.font_name]["char"][str(ord(letter))][x1+y1*width]==1):
                    if (rotation==0):
                        image.putpixel((x+x1,       y+y1       ),color)
                    if (rotation==1):
                        image.putpixel((x+height-y1,y+x1       ),color)
                    if (rotation==2):
                        image.putpixel((x+width-x1, y+height-y1),color)                        
                    if (rotation==3):
                        image.putpixel((x+y1,       y+height-x1),color)
                    if (rotation==4):
                        image.putpixel((x+x1,       y+height-y1),color)
                    if (rotation==5):
                        image.putpixel((x+y1,       y+x1       ),color)
                    if (rotation==6):
                        image.putpixel((x+width-x1, y+y1       ),color)
                    if (rotation==7):
                        image.putpixel((x+height-y1,y+height-x1),color)                                                 
    
    def puttext(self,image,text,rotation,x,y,color):
        """
        Parameters
        ----------
        image : Image
            The image where to write the letter
        text : str
            The text to write
        rotation : int
            The rotation (clockwize)
            - 0: no rotation
            - 1: 90° rotation
            - 2: 180° rotation
            - 3: 270° rotation
            - 4: vertical mirror + no rotation
            - 5: vertical mirror + 90° rotation
            - 6: vertical mirror + 180° rotation
            - 7: vertical mirror + 270° rotation
        x : int
            The x coordonate
        y : int
            The y coordonate
        color: ((red:int, blue:int, green: int))
            The letter color
        """    
        width=self.fonts[self.font_name]["width"]
        for i in range(len(text)):
            if rotation%4==0:
                self.putletter(image, text[i],rotation,x+i*width,y,        color)
            if rotation%4==1:
                self.putletter(image, text[i],rotation,x,        y+i*width,color)
            if rotation%4==2:
                self.putletter(image, text[i],rotation,x-i*width,y,        color)
            if rotation%4==3:
                self.putletter(image, text[i],rotation,x,        y-i*width,color)                
                

        